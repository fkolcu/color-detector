package com.furkank.colordetector;

public class UserColor {
    public String color;
    public String name;

    public UserColor() {
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
